package com.Conditons;

import java.util.*;

public class ButtonConditions {

	public static void main(String[] args) {

		Scanner sc = new Scanner(System.in);
		int button = sc.nextInt();

		if (button == 1) {
			System.out.println("Kamal");
		} else if (button == 2) {
			System.out.println("Gopi");
		} else if (button == 3) {
			System.out.println("Khadar");
		} else if (button == 4) {
			System.out.println("Vivek");
		} else if (button == 5) {
			System.out.println("Nissar");
		} else if (button == 6) {
			System.out.println("Chinna");
		} else if (button == 7) {
			System.out.println("Chirru");
		} else {
			System.out.println("Defualt");
		}

	}

}
