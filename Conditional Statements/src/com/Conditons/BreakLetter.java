package com.Conditons;

import java.util.*;

public class BreakLetter {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int button = sc.nextInt();

		switch (button) {

		case 1:
			System.out.println("Nissar");
			break;
		case 2:
			System.out.println("Naseer");
			break;
		case 3:
			System.out.println("Firoz");
			break;
		case 4:
			System.out.println("Eleiyaz");
			break;
		default:
			System.out.println("Any One");

		}

	}

}
